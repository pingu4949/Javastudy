package com.ict.day03;

public class Ex05 {
	public static void main(String[] args) {
		// 대입 연산자  : 특정 값이나 객체의 주소를 변수에 기억시킬 때 사용
		// ** 변수를 호출하면 변수가 가지고 있는 데이터가 나온다.
		// 변수 a = 데이터 b 또는 변수 b
		// a += b =>  a = a + b
		// a -= b => a = a - b
		
		int su1 = 7;
		
		//su1과 +4를 해서 su1에 저장하라는 뜻
		su1 += 4; // su1 = su1 + 4; //su1과 +4를 해서 su1에 저장하라는 뜻
		System.out.println(su1);
		
		//su1과 -4를 해서 su1에 저장하라는 뜻
		su1 -= 4; // su1 = su1 - 4; 
		System.out.println(su1);
		System.out.println();
		
		
		
	}
}
